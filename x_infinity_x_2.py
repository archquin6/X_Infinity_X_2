def q_star_search_inference(lat, lon, headline):
    model.eval()

    url = f"https://api.open-meteo.com/v1/forecast?latitude={lat}&longitude={lon}&past_days=5&hourly=temperature_2m,relative_humidity_2m&timezone=auto"
    response = requests.get(url).json()

    data = pd.DataFrame({
        'timestamp': pd.to_datetime(response['hourly']['time']),
        'Temperature': response['hourly']['temperature_2m'],
        'Humidity': response['hourly']['relative_humidity_2m']
    }).dropna().set_index('timestamp')

    raw_temps = data['Temperature'].values[-60:].reshape(-1, 1)
    raw_hums = data['Humidity'].values[-60:].reshape(-1, 1)

    temps = temp_scaler.transform(raw_temps)
    hums = hum_scaler.transform(raw_hums)

    combined_features = np.concatenate([temps, hums], axis=1)
    ts_input = torch.FloatTensor(combined_features).unsqueeze(0).to(device)

    encoded = tokenizer(headline, padding='max_length', truncation=True, max_length=128, return_tensors='pt')
    ids, mask = encoded['input_ids'].to(device), encoded['attention_mask'].to(device)

    with torch.no_grad():
        with torch.autocast(device_type="cuda" if torch.cuda.is_available() else "cpu", dtype=torch.bfloat16):
            policy_logits, q_values = model(ids, mask, ts_input)

            # --- Q* (PUCT) SEARCH EQUATION ---
            # Score(s, a) = Q(s, a) + C_PUCT * P(s, a)
            policy_probs = F.softmax(policy_logits, dim=-1)
            puct_scores = q_values + C_PUCT * policy_probs

            # Select best action via Tree Search
            best_action_idx = torch.argmax(puct_scores, dim=-1).item()
            best_delta_scaled = action_space[best_action_idx].item()

            current_scaled = ts_input[0, -1, 0].item()
            target_scaled = current_scaled + best_delta_scaled

    # Convert back to real temperature
    pred_actual = temp_scaler.inverse_transform([[target_scaled]])[0][0]
    return float(pred_actual), float(data['Temperature'].iloc[-1].item())

