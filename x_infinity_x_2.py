# 3. ARCHITECTURE (Q* / ALPHAZERO STYLE)
# ==========================================
class CrossAttentionFusion(nn.Module):
    def __init__(self, d_model, n_heads=8):
        super().__init__()
        self.q_proj = nn.Linear(d_model, d_model)
        self.k_proj = nn.Linear(1600, d_model)
        self.v_proj = nn.Linear(1600, d_model)
        self.mha = nn.MultiheadAttention(d_model, n_heads, batch_first=True, dropout=0.0)
        self.mix = nn.Linear(d_model * 2, d_model)
        self.norm = nn.LayerNorm(d_model)

    def forward(self, state_hidden, text_sequences):
        q = self.q_proj(state_hidden)
        k = self.k_proj(text_sequences)
        v = self.v_proj(text_sequences)
        attn_out, _ = self.mha(q, k, v)
        fused = self.mix(torch.cat([state_hidden, attn_out], dim=-1))
        return self.norm(fused)

class AlphaZero_QStar_Weather(nn.Module):
    def __init__(self, base_model, ts_features=2, d_model=128):
        super().__init__()
        self.gpt2 = base_model
        for param in self.gpt2.parameters(): param.requires_grad = False
        for param in self.gpt2.h[-2:].parameters(): param.requires_grad = True

        # LSTM Replaced with 1D-CNN Feature Extractor to build the "State"
        self.ts_extractor = nn.Sequential(
            nn.Conv1d(ts_features, 64, kernel_size=3, padding=1),
            nn.ReLU(),
            nn.Conv1d(64, d_model, kernel_size=3, padding=1),
            nn.ReLU(),
            nn.AdaptiveAvgPool1d(1) # Flatten sequence to a single State vector
        )

        self.fusion = CrossAttentionFusion(d_model)

        # --- ALPHAZERO DUAL HEADS ---
        # 1. Policy Head P(s, a): Probabilities of which action (temp shift) to take
        self.policy_head = nn.Sequential(
            nn.Linear(d_model, 256), nn.GELU(), nn.Linear(256, NUM_ACTIONS)
        )

        # 2. Value Head Q(s, a): Expected Reward (Accuracy) for every action
        self.value_head = nn.Sequential(
            nn.Linear(d_model, 256), nn.GELU(), nn.Linear(256, NUM_ACTIONS)
        )

    def forward(self, ids, mask, ts):
        gpt2_out = self.gpt2(ids, attention_mask=mask)
        tokens = gpt2_out.last_hidden_state

        # Encode TS State: (B, Seq, Feat) -> (B, Feat, Seq) for Conv1d
        ts_state = self.ts_extractor(ts.permute(0, 2, 1)).permute(0, 2, 1)

        # Fused State $s$
        fused_state = self.fusion(ts_state, tokens).squeeze(1)

        # Outputs P(s, a) and Q(s, a)
        policy_logits = self.policy_head(fused_state)
        q_values = self.value_head(fused_state)
        return policy_logits, q_values

# ==========================================
# 4. TRAINING (DUAL-LOSS RL CLONING)
# ==========================================
model = AlphaZero_QStar_Weather(base_model=gpt2_base).to(device)

optimizer = optim.AdamW([
    {'params': model.gpt2.parameters(), 'lr': 5e-5},
    {'params': model.ts_extractor.parameters(), 'lr': 1e-4},
    {'params': model.fusion.parameters(), 'lr': 5e-4},
    {'params': model.policy_head.parameters(), 'lr': 1e-4},
    {'params': model.value_head.parameters(), 'lr': 1e-4}
])

print(f"AlphaZero Model Initialized. Trainable Params: {sum(p.numel() for p in model.parameters() if p.requires_grad):,}")

for epoch in range(EPOCHS):
    model.train()
    loop = tqdm(dataloader, desc=f"Epoch {epoch+1}")
    for ids, mask, ts, y in loop:
        ids, mask, ts, y = ids.to(device), mask.to(device), ts.to(device), y.to(device)
        optimizer.zero_grad()

        policy_logits, q_values = model(ids, mask, ts)

        # --- Q-LEARNING MATHEMATICS ---
        # Calculate true Delta T
        current_temp_scaled = ts[:, -1, 0]
        true_delta = y.squeeze(-1) - current_temp_scaled

        # Map True Delta to Target Action Bin for the Policy Head
        target_action = ((true_delta - ACTION_MIN) / (ACTION_MAX - ACTION_MIN) * (NUM_ACTIONS - 1)).round().long()
        target_action = torch.clamp(target_action, 0, NUM_ACTIONS - 1)

        # Calculate True Q-Values (Reward) for ALL actions
        # Reward = Negative absolute error. Perfect guess = 0 error.
        all_actions = action_space.unsqueeze(0).repeat(ts.size(0), 1)
        true_q_values = -torch.abs(all_actions - true_delta.unsqueeze(1))

        # Dual Losses
        loss_policy = F.cross_entropy(policy_logits, target_action)
        loss_value = F.mse_loss(q_values, true_q_values)
        loss = loss_policy + loss_value

        loss.backward()
        optimizer.step()
        loop.set_postfix(Pol_L=loss_policy.item(), Val_L=loss_value.item())

