# ==========================================
# 1. HYPERPARAMETERS & ALPHAZERO CONFIG
# ==========================================
TS_FEATURES = 2
TS_SEQ_LEN = 60
FORECAST_HORIZON = 12
EPOCHS = 1
BATCH_SIZE = 4

# --- Q* / ALPHAZERO ACTION SPACE ---
# We discretize the temperature shift into 201 actions
# representing scaled changes from -1.0 to +1.0
NUM_ACTIONS = 201
ACTION_MIN = -1.0
ACTION_MAX = 1.0
C_PUCT = 1.25   # AlphaZero exploration constant for Q* Search

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
action_space = torch.linspace(ACTION_MIN, ACTION_MAX, NUM_ACTIONS).to(device)

# ==========================================
# 2. DATA PIPELINE (FORWARD-LOOKING)
# ==========================================
print("Fetching 90 days of TS data (Temperature + Humidity) from Open-Meteo...")
LAT, LON = 40.7128, -74.0060
url = f"https://api.open-meteo.com/v1/forecast?latitude={LAT}&longitude={LON}&past_days=90&hourly=temperature_2m,relative_humidity_2m&timezone=auto"
response = requests.get(url).json()

ts_df = pd.DataFrame({
    'timestamp': pd.to_datetime(response['hourly']['time']),
    'Temperature': response['hourly']['temperature_2m'],
    'Humidity': response['hourly']['relative_humidity_2m']
})
ts_df = ts_df.dropna().set_index('timestamp').sort_index()
ts_df.index = ts_df.index.tz_localize(None)

ts_df['Future_Change'] = ts_df['Temperature'].shift(-FORECAST_HORIZON) - ts_df['Temperature']
ts_df = ts_df.dropna(subset=['Future_Change'])

p10 = ts_df['Future_Change'].quantile(0.10)
p40 = ts_df['Future_Change'].quantile(0.40)
p60 = ts_df['Future_Change'].quantile(0.60)
p90 = ts_df['Future_Change'].quantile(0.90)

def get_dense_freetext(x):
    hot_phrases =["massive heat front", "scorching temperatures", "unprecedented warming", "boiling heatwave"]
    cold_phrases =["severe arctic blast", "drastic drop in temperatures", "freezing blizzard", "sub-zero tundra"]
    stable_phrases =["completely stable", "mild and calm", "normal diurnal weather", "no significant changes"]

    hot = 1.0 if x >= p90 else (0.0 if x <= p60 else (x - p60) / (p90 - p60))
    cold = 1.0 if x <= p10 else (0.0 if x >= p40 else (p40 - x) / (p40 - p10))

    if hot > 0.6 and hot > cold:
        return f"Meteorologists warn of a {random.choice(hot_phrases)} approaching the area over the next 12 hours."
    elif cold > 0.6 and cold > hot:
        return f"Expect a {random.choice(cold_phrases)} moving in rapidly over the next 12 hours."
    else:
        return f"Conditions remain {random.choice(stable_phrases)}."

ts_df['text'] = ts_df['Future_Change'].apply(get_dense_freetext)
ts_df = ts_df.drop(columns=['Future_Change'])

temp_scaler = MinMaxScaler()
hum_scaler = MinMaxScaler()

ts_df['Scaled_Temp'] = temp_scaler.fit_transform(ts_df[['Temperature']])
ts_df['Scaled_Hum'] = hum_scaler.fit_transform(ts_df[['Humidity']])

print("Loading GPT-2 XL Tokenizer & Base Model...")
tokenizer = GPT2Tokenizer.from_pretrained('gpt2-xl')
tokenizer.pad_token = tokenizer.eos_token
gpt2_base = GPT2Model.from_pretrained('gpt2-xl').to(device)

class WeatherQuantumDataset(Dataset):
    def __init__(self, ts_df, tokenizer, ts_seq_len=60, horizon=12):
        self.ts_df = ts_df
        self.tokenizer = tokenizer
        self.ts_seq_len = ts_seq_len
        self.horizon = horizon
        self.ts_values = self.ts_df[['Scaled_Temp', 'Scaled_Hum']].values
        self.text_values = self.ts_df['text'].values

    def __len__(self):
        return len(self.ts_values) - self.ts_seq_len - self.horizon

    def __getitem__(self, idx):
        ts_window = self.ts_values[idx : idx + self.ts_seq_len]
        current_idx = idx + self.ts_seq_len - 1
        target_idx = current_idx + self.horizon

        target_val = self.ts_values[target_idx, 0]
        latest_text = self.text_values[current_idx]

        encoded_text = self.tokenizer(latest_text, padding='max_length', truncation=True, max_length=128, return_tensors='pt')
        return (encoded_text['input_ids'].squeeze(),
                encoded_text['attention_mask'].squeeze(),
                torch.FloatTensor(ts_window),
                torch.FloatTensor([target_val]))

dataset = WeatherQuantumDataset(ts_df, tokenizer, ts_seq_len=TS_SEQ_LEN, horizon=FORECAST_HORIZON)
dataloader = DataLoader(dataset, batch_size=BATCH_SIZE, shuffle=True)

